#include<stdio.h>
int main(){
    int x=5;
    printf("%d\n",x);
    x+=3;
    printf("%d\n",x);
    x-=3;
    printf("%d\n",x);
    x*=3;
    printf("%d\n",x);
    x/=3;
    printf("%d\n",x);
    x%=3;
    printf("%d\n",x);
    x&=3;
    printf("%d\n",x);
    x|=3;
    printf("%d\n",x);
    x^=3;
    printf("%d\n",x);
    x>>=3;
    printf("%d\n",x);
    x<<=2;
    printf("%d\n",x);
}